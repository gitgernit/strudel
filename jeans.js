const bass = note("f#2@6 f#2@6 f#2@8 c#2@6 c#2@6 d2@6 d2@6 d2@8 d2@6 d2@6")
  .sound("triangle")
  .detune(-1)
  .decay(.6)
  .adsr(".1:.6:0:0")

const low = bass.lpf(200).lpq(10).gain(4)
const mid = bass.bpf(1000).bpq(10).gain(2)
const high = bass.hpf(2000).hpq(10).gain(1)

const bassStack = stack(low, mid, high)

const hihats = sound("hh")
  .bank("rolandtr808")
  .struct("x!64")
  .room(0.15)
  .gain(0.3)

const kicks = sound("bd:7")
  .bank("rolandtr808")
  .struct("x -!5 x -!5 x -!7 x -!5 x -!5 x -!5 x -!5 x -!7 x -!5 x -!5")
  .gain(2)

const claps = sound("clap:4")
  .struct("-!8 x -!15 x -!5 x -!9 x -!15 x -!5 x -")
  .decay(0.4)
  .sustain(0)
  .gain(0.3)

const synths1 = note(`
  f#3 c#4 e4 c#4 f#3 c#4 e4 c#4 f#3 c#4 e4 c#4 f#3 c#4 e4 c#4 f#3 c#4 e4 c#4
  c#3 c#4 e4 c#4 c#3 c#4 e4 c#4 c#3 c#4 e4 c#4
  d3 c#4 e4 c#4 d3 c#4 e4 c#4 d3 c#4 e4 c#4 d3 c#4 e4 c#4 d3 c#4 e4 c#4 d3 c#4 e4 c#4 d3 c#4 e4 c#4 d3 c#4 e4 c#4
`)
  .sound("square")
  .decay(.25)
  .detune(-1)
  .crush(4)

const synths2 = note(`
  - a4 g#4 e4 - a4 g#4 e4 - a4 g#4 e4 - a4 g#4 e4 - a4 g#4 e4 - a4 g#4 e4 - a4 g#4 e4 - a4 g#4 e4
  - a4 g#4 e4 - a4 g#4 e4 - a4 g#4 e4 - a4 g#4 e4 - a4 g#4 e4 - a4 g#4 e4 - a4 g#4 e4 - a4 g#4 e4
`)
  .sound("square")
  .decay(0.25)
  .room(.1)
  .detune(-1)

main: stack(
  bassStack,
  hihats,
  kicks,
  claps,
  synths1,
  synths2
).gain("[4!112 0.9375 0.875 0.8125 0.75 0.6875 0.625 0.5625 0.5 0.4375 0.375 0.3125 0.25 0.125 0.0625 0] / 1.5")
 .cpm(9)
 .scope({
   scale: .05,
   pos: .5
 })

