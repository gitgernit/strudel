const patterns = [
  "g#3 -!3 c4 -!3 c#4 -!3 a#3 -!3",
  "- f4 g#4 c5 - g#4 c5 -!2 g#4 c#5 -!2 g#4 c#5 c5",
  "c6@2 f5@2 g#5@2 - g#5@0.5 -@0.5 -!4 d#6 c#6 c6 g#5",
  "-!7 d#5 -!3 d#5 -!4, -@7.5 f5@3.5 f5 -!4",
].map(
  p => note(p)
    .sound("piano")
    .attack(.025)
    .decay(.5)
    .sustain(.8)
    .release(.5)
)

main: stack(
  ...patterns,
).cpm(60)
 .gain(3)
 .slow(6)
 .transpose(-10)
 .lpf(3000)
 .pianoroll({
   vertical: true,
   fold: false,
 })

